#include "Cubemap.h"

Cubemap::Cubemap()
{
    fillVertices();
}
void Cubemap::connectShader(const std::string &vertexPath, const std::string &fragmentPath)
{
    shader = new Shader(vertexPath.c_str(), fragmentPath.c_str());
}
std::vector<float> &Cubemap::getVertices()
{
    return this->vertices;
}
Shader Cubemap::getShader()
{
    return *shader;
}
